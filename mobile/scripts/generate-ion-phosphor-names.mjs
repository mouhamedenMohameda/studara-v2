function pick(ion) {
  if (OVERRIDE[ion] && PH.has(OVERRIDE[ion])) return OVERRIDE[ion];

  const normalized = ion.replace(/^ios-/, '').replace(/^md-/, '');
  if (normalized !== ion) {
    const r = pick(normalized);
    if (r !== 'CircleIcon') return r;
  }

  const tryPascal = (s) => {
    const n = pascalFromKebab(s) + 'Icon';
    return PH.has(n) ? n : null;
  };

  let cur = ion.replace(/-sharp$/, '').replace(/-outline$/, '');
  for (let depth = 0; depth < 12; depth++) {
    const hit = tryPascal(cur);
    if (hit) return hit;
    if (!cur.includes('-')) break;
    cur = cur.replace(/-[a-z0-9]+$/, '');
  }

  cur = ion.replace(/-sharp$/, '');
  for (let depth = 0; depth < 12; depth++) {
    const hit = tryPascal(cur);
    if (hit) return hit;
    if (!cur.includes('-')) break;
    cur = cur.replace(/-[a-z0-9]+$/, '');
  }

  if (OVERRIDE[ion] && PH.has(OVERRIDE[ion])) return OVERRIDE[ion];

  const baseCandidates = [
    ion.replace(/-sharp$/, '').replace(/-outline$/, ''),
    ion.replace(/-outline$/, '').replace(/-sharp$/, ''),
    ion.replace(/-circle-outline$/, '').replace(/-circle-sharp$/, '').replace(/-circle$/, ''),
    ion.replace(/-outline$/, ''),
    ion.replace(/-sharp$/, ''),
  ];
  for (const b of baseCandidates) {
    if (b && OVERRIDE[b] && PH.has(OVERRIDE[b])) return OVERRIDE[b];
  }

  return 'CircleIcon';
}

const out = {};
for (const ion of ionKeys) {
  out[ion] = pick(ion);
}

