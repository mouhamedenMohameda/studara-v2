import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

const ionPath = path.join(
  root,
  'node_modules/@expo/vector-icons/build/vendor/react-native-vector-icons/glyphmaps/Ionicons.json',
);
const ionKeys = Object.keys(JSON.parse(fs.readFileSync(ionPath, 'utf8'))).sort();

const iconsDir = path.join(root, 'node_modules/phosphor-react-native/lib/typescript/icons');
const names = fs
  .readdirSync(iconsDir)
  .filter((f) => f.endsWith('.d.ts'))
  .map((f) => {
    const t = fs.readFileSync(path.join(iconsDir, f), 'utf8');
    const m = t.match(/as (\w+Icon)\b/);
    return m ? m[1] : null;
  })
  .filter(Boolean);
const PH = new Set(names);

function pascalFromKebab(s) {
  return s
    .split('-')
    .map((p) => (p ? p[0].toUpperCase() + p.slice(1) : ''))
    .join('');
}

