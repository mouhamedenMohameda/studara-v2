// Builds src/icons/map.ionicons.auto.ts from the official Ionicons glyph map (all icons).
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { createRequire } from 'module';

const require = createRequire(import.meta.url);
const __dirname = path.dirname(fileURLToPath(import.meta.url));

const glyphMap = require(
  '../node_modules/@expo/vector-icons/build/vendor/react-native-vector-icons/glyphmaps/Ionicons.json',
);

function kebabToCamel(s) {
  const c = s.replace(/-([a-z0-9])/g, (_, ch) => ch.toUpperCase());
  if (/^[0-9-]/.test(c) || !/^[$A-Za-z_]/.test(c)) return `_${c.replace(/[^a-zA-Z0-9_]/g, '_')}`;
  return c;
}

const keyToGlyph = new Map();
for (const glyph of Object.keys(glyphMap).sort()) {
  let key = kebabToCamel(glyph);
  if (keyToGlyph.has(key)) {
    let n = 2;
    while (keyToGlyph.has(`${key}_${n}`)) n += 1;
    key = `${key}_${n}`;
  }
  keyToGlyph.set(key, glyph);
}

const lines = [...keyToGlyph.entries()].map(([key, glyph]) => `  ${key}: '${glyph}',`);

const out = `/**
 * AUTO-GENERATED by scripts/generate-ion-icon-map.mjs — do not edit by hand.
 * Every Ionicons glyph is exposed as AppIconName (camelCase → kebab file name).
 */
import type { Ionicons } from '@expo/vector-icons';

export const ION_ICON_MAP = {
${lines.join('\n')}
} as const satisfies Record<string, keyof typeof Ionicons.glyphMap>;

export type AppIconName = keyof typeof ION_ICON_MAP;
`;

const outPath = path.join(__dirname, '..', 'src', 'icons', 'map.ionicons.auto.ts');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, out);
console.log('Wrote', outPath, 'keys:', keyToGlyph.size);
